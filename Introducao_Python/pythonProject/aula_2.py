
a = int(input("Insira um valor para A: "))
b = int(input("Insira um valor para B: "))
soma = a + b
subtracao = a - b
multiplicacao = a * b
divisao = a/b
resto = a % b

print(f'Valor de A: {a}')
print(f'Valor de B: {b}')
print(f'Soma: {soma}')
print(f'Subtração: {subtracao}')
print(f'Multiplicação: {multiplicacao}')
print(f'Divisão: {divisao:.2f}')
print(f'Resto: {resto}')
